    public void cargarDatosTabla() {
        String[] columnas = {"Nombre", "Descripcion", "Tipos", "Valores/restriccion"};
        modelo_tabla = new DefaultTableModel();
        modelo_tabla.setColumnIdentifiers(columnas);
        this.tabla_variables.setModel(modelo_tabla);
        
        ArrayList<Variable> variables_cargar = this.variables.getVariables();
        for(Variable variable : variables_cargar){
            if(variable.getTipo() == 'E'){
                String[] data = {variable.getNombre(), variable.getDescripcion(), "Escalar", ((Escalar)variable).getConjuntoValores().toString()};
                this.modelo_tabla.addRow(data);
            }else{
                Numerica varnum = (Numerica)variable;
                String restriccion = "";
                if(varnum.isHasMaxIntervalo()) restriccion = "max: "+varnum.getMaxIntervalo();
                if(varnum.isHasMinIntervalo()) restriccion += ", min: "+varnum.getMinIntervalo();
                String[] data = {variable.getNombre(), variable.getDescripcion(), "Numerica", restriccion};
                this.modelo_tabla.addRow(data);
            }
        }
    }
