    private void eliminar_variableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminar_variableActionPerformed
        int index = this.tabla_variables.getSelectedRow();
        if(index == -1) return;
        
        Variable variable = this.variables.getVariable(index);
        boolean esUsado = ((MainFrame)parent).reglas.variableEsUsada(variable);
        if(esUsado){
            JOptionPane.showMessageDialog(null, "No se puede eliminar la variable, es usada en una regla");
            return;
        } 
        
        this.variables.removeVariable(variables.getVariables().get(index).getNombre());
        this.cargarDatosTabla();
    }//GEN-LAST:event_eliminar_variableActionPerformed

